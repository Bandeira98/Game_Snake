#Escreva um programa que converta uma temperatura de Celsius para Fahrenheit. A fórmula de conversão é: F = C * 9/5 + 32.

#entrada de dados
celsius = float(input("digite a temperatura em graus °C:"))

#processamento
f = celsius * (9/5) + 32

#saida de dados
print ("temperatura em °f -> ",f)